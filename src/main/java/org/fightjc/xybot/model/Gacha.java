package org.fightjc.xybot.model;

public class Gacha {

    String title;

    String content;

    public Gacha(String title, String content) {
        this.title = title;
        this.content = content;
    }

    public String getTitle() {
        return title;
    }

    public String getContent() {
        return content;
    }
}
